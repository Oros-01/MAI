#include <iostream>
#include <cstring>
using namespace std;
/*
1) Что такое size_t и зачем присваивать n = strlen(words)?

size_t — это целочисленный тип без знака (unsigned), который используется для представления размеров и индексов объектов в памяти (длина массива, возвращаемое sizeof, результат strlen и т.п.). Он гарантировано достаточен, чтобы вместить размер самой большой возможной области памяти на данной платформе.
strlen возвращает значение типа size_t. Поэтому хорошо хранить длину именно в size_t n, чтобы не смешивать signed/unsigned и не получать предупреждений компилятора.

Почему n = strlen(words) полезно (а не вызывать strlen каждый раз в заголовке цикла):

strlen — это O(n) операция: она проходит по строке до '\0', считая символы. Если ты вызываешь strlen(words) в условии цикла на каждой итерации — ты каждый раз заново пересчитываешь длину. Это лишняя трата времени.

Читаемость: n — одно место, где хранится длина. Если позже строка меняется, можно явно обновить n (или заново вызвать strlen там, где нужно).

Типовая безопасность: size_t n = strlen(words); делает типы согласованными (нет неявных преобразований).
*/


void my_sort(char* words){
    int n = strlen(words);// создаем массив
    /*  
    открываем первый цикл по words и останавливаясь на первой букве "b"
    открываем второй цикл по words и пока в первом стоим на первой букве "b" здесь открываем аналогичный
    цикл и у нас получается в из первой b открывается новый banana и в нём проходятся по всем буквам,
    потом мы попадаем во вторую a(из главного цикла) и также проходимся по всем буквам.
    ! Во время этого прохождения мы каждый раз сравниваем соседние буквы и если левая больше, то мы переставляем их местами
    */
    for (int i = 0; i < n; i=i+1){ 
        for (int j = i; j < n-1; j=j+1){ 
            if (words[j] > words[j+1]) {
                swap (words[j], words[j+1]);
            }
        }
    }
}


int main(){
    char words[10] = "banana"; // создаем массив
    // запускаем цикл по длине строки массива
    // cout << int(words[i]) << (words[i]) << endl; // выводим int-вое значение буквы массива(по ASCII)
    cout << words << endl;
    my_sort(words);
    cout << words << endl;

    return 0;
}

